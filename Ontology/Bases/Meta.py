# --------------------------------------------------------------------------------------------------
# ----------------------------------- Ontology :: Bases :: Meta ------------------------------------
# --------------------------------------------------------------------------------------------------
from typing import TYPE_CHECKING
from typing import Protocol
from typing import Dict
from typing import Union
from typing import ForwardRef
from typing import KeysView
from typing import ItemsView
from typing import ValuesView
from typing import Iterator
from typing import Optional
from typing import runtime_checkable

if TYPE_CHECKING:
    from . Node import Node
else:
    Node = ForwardRef('Node')

T_Key = str
T_Val = Union[bool, int, float, Node]


# --------------------------------------------------------------------------------------------------
# ---------------------------------------- Protocol :: Meta ----------------------------------------
# --------------------------------------------------------------------------------------------------
@runtime_checkable
class Meta(Protocol):

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Operator :: Get Item ----------------------------------
    # ------------------------------------------------------------------------------------------
    def __getitem__(self, key: T_Key) -> T_Val:
        ...

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Operator :: Set Item ----------------------------------
    # ------------------------------------------------------------------------------------------
    def __setitem__(self, key: T_Key, value: T_Val) -> None:
        ...

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Operator :: Del Item ----------------------------------
    # ------------------------------------------------------------------------------------------
    def __delitem__(self, key: T_Key) -> None:
        ...

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Operator :: Iterator ----------------------------------
    # ------------------------------------------------------------------------------------------
    def __iter__(self) -> Iterator[T_Key]:
        ...

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Operator :: Get Item ----------------------------------
    # ------------------------------------------------------------------------------------------
    def __len__(self) -> int:
        ...

    # ------------------------------------------------------------------------------------------
    # --------------------------------- Operator :: Truthiness ---------------------------------
    # ------------------------------------------------------------------------------------------
    def __bool__(self) -> bool:
        ...

    # ------------------------------------------------------------------------------------------
    # --------------------------------- Operator :: Membership ---------------------------------
    # ------------------------------------------------------------------------------------------
    def __contains__(self, key: T_Key) -> bool:
        ...

    # ------------------------------------------------------------------------------------------
    # ----------------------------------- Method :: Get Keys -----------------------------------
    # ------------------------------------------------------------------------------------------
    def keys(self) -> KeysView[T_Key]:
        ...

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Method :: Get Values ----------------------------------
    # ------------------------------------------------------------------------------------------
    def values(self) -> ValuesView[T_Val]:
        ...


    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Method :: Get Items -----------------------------------
    # ------------------------------------------------------------------------------------------
    def items(self) -> ItemsView[T_Key, T_Val]:
        ...

    # ------------------------------------------------------------------------------------------
    # ------------------------------------- Method :: Copy -------------------------------------
    # ------------------------------------------------------------------------------------------
    def copy(self) -> 'Meta':
        ...

    # ------------------------------------------------------------------------------------------
    # --------------------------------- Method :: Set Default ----------------------------------
    # ------------------------------------------------------------------------------------------
    def setdefault(self, key: T_Key, default: T_Val) -> T_Val:
        ...

    # ------------------------------------------------------------------------------------------
    # --------------------------------- Method :: Set Default ----------------------------------
    # ------------------------------------------------------------------------------------------
    def setdefault(self, other: Dict[T_Key, T_Val]) -> None:
        ...

    # ------------------------------------------------------------------------------------------
    # ------------------------------------ Method :: Pop ---------------------------------------
    # ------------------------------------------------------------------------------------------
    def pop(self, key: T_Key, default: Optional[T_Val] = None) -> T_Val:
        ...

    # ------------------------------------------------------------------------------------------
    # ---------------------------------- Method :: Pop Item ------------------------------------
    # ------------------------------------------------------------------------------------------
    def popitem(self) -> tuple[T_Key, T_Val]:
        ...

    # ------------------------------------------------------------------------------------------
    # ------------------------------------ Method :: Clear -------------------------------------
    # ------------------------------------------------------------------------------------------
    def clear(self) -> None:
        ...